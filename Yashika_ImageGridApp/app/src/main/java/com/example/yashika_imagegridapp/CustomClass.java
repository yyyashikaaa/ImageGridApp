package com.example.yashika_imagegridapp;

import android.graphics.Bitmap;

public class CustomClass {
    Bitmap bitmap;
    String time;

    public CustomClass(Bitmap image) {
        this.bitmap = image;
    }

    public Bitmap getImage() {
        return bitmap;
    }
    public Bitmap getBitmap() {
        return bitmap;
    }

    public String getTime() {
        return time;
    }
}
